function generate_toric_code_PEPS()
    A = zeros(2,2,2,2,2)
    for p in 1:2
        if p == 1
            A[1,1,1,1,1] = 1
            A[2,2,2,2,1] = 1
            A[1,1,2,2,1] = 1
            A[2,2,1,1,1] = 1
        end
        if p == 2
            A[1,2,1,2,2] = 1
            A[2,1,2,1,2] = 1
            A[1,2,2,1,2] = 1
            A[2,1,1,2,2] = 1
        end
    end

    B = zeros(2,2,2,2,2)
    for p in 1:2
        if p == 1
            B[1,1,1,1,1] = 1
            B[2,2,2,2,1] = 1
            B[1,2,2,1,1] = 1
            B[2,1,1,2,1] = 1
        end
        if p == 2
            B[1,1,2,2,2] = 1
            B[2,2,1,1,2] = 1
            B[1,2,1,2,2] = 1
            B[2,1,2,1,2] = 1
        end
    end
    return A, B
end

